# Trap Ctrl+C and cleanup
cleanup() {
    echo ""
    echo -e "${YELLOW}Stopping services...${NC}"
    kill $CORE_PID $AI_PID $FRONTEND_PID 2>/dev/null
    echo -e "${GREEN}All services stopped. Logs saved to $LOG_DIR${NC}"
